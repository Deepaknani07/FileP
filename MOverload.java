class MOverload
{
	int add(int a,int b)
	{
		int sum=a+b;
		return sum;
		
		
	}
	int add(int a,int b,int c) 
	{
		int sum=a+b+c;
		return sum;
		
		
	}
	float add(float a ,float b)
	{
		float sum=a+b;
		return sum;
	}
}
public class MOverlaod {

	public static void main(String[] args)
	{
MOverload obj = new MOverload();
int sum1=obj.add(10,20);
int sum2 = obj.add(10, 20,30);
float sum3 = obj.add(2.2f,2.2f);
System.out.println("method of overload sum1="+sum1);
System.out.println("method of overload sum2="+sum2);
System.out.println("method of overload sum3="+sum3);

	}

}
